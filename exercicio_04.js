// Faça um Programa que peça as 4 notas bimestrais e mostre a média.


alert('Calculando e apresentando a média de 4 notas bimestrais')

let primeiraNota = Number(prompt('Digite a primeira nota: ')) // 10
let segundaNota = Number(prompt('Digite a segunda nota: ')) // 7.5
let terceiraNota = Number(prompt('Digite a terceira nota: ')) // 9.3
let quartaNota = Number(prompt('Digite a quarta nota: ')) // 2.7

alert(`A média das notas apresentadas é ${(primeiraNota+segundaNota+terceiraNota+quartaNota)/4}`)