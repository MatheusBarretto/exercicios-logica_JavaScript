/*
Faça um Programa que: 
1. peça a temperatura em graus Celsius
2. transforme
3. mostre em graus Farenheit.
*/

alert('Tranformando graus Celsius para Fahrenheit')

let grausCelcios = Number(prompt('Digite uma temperatura em °C'))

alert(`${grausCelcios}°C é equivalente á ${(grausCelcios*1.8)+32}°F`)